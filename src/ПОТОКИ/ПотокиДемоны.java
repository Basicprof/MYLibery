public class ПотокиДемоны {
//Демоном называется поток, предоставляющий некоторый сервис,
// работая в фоновом режиме во время выполнения программы,
// но при этом не является ее неотъемлемой частью. Таким образом,
// когда все потоки не-демоны заканчивают свою деятельность, программа завершается.
// И наоборот, если существуют работающие потоки не-демоны, программа продолжает выполнение.
// Существует, например, поток не-демон, выполняющий метод main().
}
